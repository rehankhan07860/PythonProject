def mul(x,y):
    """
    This is a function namely mul that takes two numbers as input and returns the multiplication of the numbers.
    """
    return x * y

def sum(x,y,z):
    '''
    This is a function that takes  threee numbers as innput and returns the sum of the numbers
    '''
    return x + y + z

